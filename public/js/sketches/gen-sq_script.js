var USE_SVG = false;

var WIDTH = 512;
var HEIGHT = 512;
var CENTER_X = WIDTH / 2;
var CENTER_Y = HEIGHT / 2; 

var SIDE_LENGTH = 11;
var CHAR_WIDTH = 4;
var CHAR_HEIGHT = 7;
var CHAR_PADDING = 0.5;

var TIME_BLOCK_SIZE = WIDTH / ((CHAR_WIDTH * 4) + (CHAR_WIDTH * CHAR_PADDING * 3));
var SQUARE_BLOCK_SIZE = WIDTH / SIDE_LENGTH;

var blocks = [];
var squarePositions = [];
var blockCount;

var colorOn;
var colorOff;

function setup() {
  if (USE_SVG) {
    let myCanvas = createCanvas(WIDTH, HEIGHT, SVG);
  } else {
    let myCanvas = createCanvas(WIDTH, HEIGHT);
    myCanvas.parent('sketchContainer');
    myCanvas.addClass('sketch');
  }

  textAlign(CENTER, CENTER);

  blockCount = SIDE_LENGTH * SIDE_LENGTH;
  for (var i = 0; i < blockCount; i++) {
    blocks[i] = getBlock();
  }

  var b = 0;
  for (var i = 0; i < SIDE_LENGTH; i++) {
    for (var j = 0; j < SIDE_LENGTH; j++) {
      squarePositions[b++] = {x: i, y: j};
    }
  }

  colorOn = color(108, 108, 128);
  colorOff = color(255, 255, 255);
  updateTime();
}

function shuffleSquarePos() {
  shuffle(squarePositions, true);
}

function draw() {
  blendMode(BLEND);
  background(255);
  blendMode(DIFFERENCE);
  fill(0);
  noStroke();

  var howSquare = abs(cos(millis() / 3000));
  // var howSquare = 0;
  if (howSquare < 0.01) {
    shuffleSquarePos();
  }

  if (howSquare > 0.99) {
    updateTime();
  }

  var blockPos = 0;
  for (var b = 0; b < 4; b++) {
    for (var j = 0; j < CHAR_HEIGHT; j++) {
      for (var i = 0; i < CHAR_WIDTH; i++) {
        var timePosX = i * TIME_BLOCK_SIZE + (b * TIME_BLOCK_SIZE * CHAR_WIDTH * 1.5);
        var timePosY = j * TIME_BLOCK_SIZE;

        drawBlock(blockPos, howSquare, timePosX, timePosY);
        
        blockPos++;
      }
    }
  }

  for (var td = 0; td < 9; td++) {
    var timePosX = TIME_BLOCK_SIZE * CHAR_WIDTH * 2.75  - (TIME_BLOCK_SIZE / 2);
    var timePosY = td < 5 ? TIME_BLOCK_SIZE * 2 : TIME_BLOCK_SIZE * 4;

    drawBlock(blockPos, howSquare, timePosX, timePosY);

    blockPos++;
  }

  if (USE_SVG) {
    noLoop();
    save("gen-sq.svg");
  } 
}

function drawBlock(block, howSquare, timeX, timeY) {
  var verticalTimeOffset = (HEIGHT - (TIME_BLOCK_SIZE * 7)) * 0.5;

  var timePosX = timeX;
  var timePosY = timeY + verticalTimeOffset;

  var squarePosX = squarePositions[block].x * SQUARE_BLOCK_SIZE;
  var squarePosY = squarePositions[block].y * SQUARE_BLOCK_SIZE;

  var positionProgress = map(howSquare, 0, 0.95, 0, 1, true);
  positionProgress = map(positionProgress, 0.2, 1, 0, 1, true);

  var colorProgress = map(positionProgress, 0, 1, 0, 1, true);
  var blockColor = blocks[block] ? colorOn : lerpColor(colorOff, colorOn, colorProgress);
  fill(blockColor);

  square(
    lerp(timePosX, squarePosX, positionProgress),
    lerp(timePosY, squarePosY, positionProgress),
    lerp(TIME_BLOCK_SIZE, SQUARE_BLOCK_SIZE, positionProgress));
}

function updateTime() {
  blocks.splice(0, 28, ...SEVEN_PLUS.glyphs[floor(hour() / 10)].pixels.flat(1));
  blocks.splice(28, 28, ...SEVEN_PLUS.glyphs[floor(hour() % 10)].pixels.flat(1));
  blocks.splice(56, 28, ...SEVEN_PLUS.glyphs[floor(minute() / 10)].pixels.flat(1));
  blocks.splice(84, 28, ...SEVEN_PLUS.glyphs[floor(minute() % 10)].pixels.flat(1));
}

